package bitm.basis.firebaseauthor;

public class RViewData {

    String mName;
    String mTime;

    public RViewData(String mName, String mTime) {
        this.mName = mName;
        this.mTime = mTime;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmTime() {
        return mTime;
    }

    public void setmTime(String mTime) {
        this.mTime = mTime;
    }
}
